    <h1>FICHE DE POSTE</h1>
    <h1>Responsable des Moyens Généraux et de la Logistique</h1>
    <h1>NÉHÉMIE INTERNATIONAL</h1>
    <p class="tagline">"Levons-nous et bâtissons !"</p>

    <h2>Contexte du poste</h2>
    <p>Ce poste s'inscrit dans le cadre du Plan de Relance de l'ONG Néhémie International (2025), au sein du pôle Administration et Opérations. Il constitue un élément essentiel dans la mise en place d'une infrastructure opérationnelle fiable et efficace, permettant la réalisation des projets et activités de l'ONG.</p>
    
    <p><strong>Position hiérarchique :</strong> Rattachement direct au Secrétaire Général /Président l'ONG</p>
    
    <p><strong>Collaboration principale :</strong> Membre du Comité Opérationnel de Relance (COR)</p>

    <h2 class="section-title">IMPORTANCE STRATÉGIQUE DU RÔLE</h2>
    <p>Le/La Responsable des Moyens Généraux et de la Logistique occupe une position cruciale dans le soutien opérationnel de toutes les activités de l'ONG. Ce poste constitue :</p>
    
    <ul>
        <li><strong>L'épine dorsale matérielle</strong> permettant la réalisation concrète des projets et initiatives</li>
        <li><strong>Un garant de l'efficience</strong> dans l'utilisation des ressources matérielles limitées</li>
        <li><strong>Un facilitateur essentiel</strong> pour la bonne exécution des activités sur le terrain</li>
        <li><strong>Un pilier de la sécurité</strong> et du bon fonctionnement des infrastructures de l'ONG</li>
    </ul>

    <h2>MISSIONS ET RESPONSABILITÉS</h2>

    <h3>1. Gestion des infrastructures et équipements</h3>
    <ul>
        <li>Superviser la gestion, l'entretien et la sécurité des locaux et espaces de travail</li>
        <li>Planifier et coordonner les travaux de maintenance préventive et curative</li>
        <li>Gérer les équipements, le mobilier et les fournitures de l'ONG</li>
        <li>Veiller au fonctionnement optimal des réseaux (électricité, eau, internet, téléphonie)</li>
        <li>Assurer la conformité des installations aux normes de sécurité et d'accessibilité</li>
    </ul>

    <h3>2. Logistique événementielle et opérationnelle</h3>
    <ul>
        <li>Organiser la logistique des événements et activités (transport, matériel, installation)</li>
        <li>Coordonner les aspects logistiques des projets terrain, notamment "Donnez-leur vous-même à manger"</li>
        <li>Gérer les déplacements et missions du personnel (véhicules, réservations, itinéraires)</li>
        <li>Anticiper les besoins logistiques des différents départements et projets</li>
        <li>Élaborer des plans logistiques pour les actions d'envergure</li>
    </ul>

    <h3>3. Gestion des achats et approvisionnements</h3>
    <ul>
        <li>Définir et mettre en œuvre une politique d'achat efficace et transparente</li>
        <li>Établir et maintenir un réseau de fournisseurs fiables et éthiques</li>
        <li>Gérer les processus d'appels d'offres et négocier les contrats avec les prestataires</li>
        <li>Assurer le suivi des commandes et la gestion des stocks</li>
        <li>Optimiser les coûts tout en maintenant la qualité des biens et services</li>
    </ul>

    <h3>4. Administration et gestion des ressources</h3>
    <ul>
        <li>Élaborer et suivre le budget des moyens généraux et de la logistique</li>
        <li>Gérer l'inventaire des actifs matériels de l'ONG</li>
        <li>Mettre en place des procédures de gestion des ressources matérielles</li>
        <li>Assurer le suivi administratif des contrats liés aux services généraux</li>
        <li>Produire des rapports réguliers sur l'utilisation des ressources matérielles</li>
    </ul>

    <h2>PROFIL ET COMPÉTENCES REQUISES</h2>

    <h3>Formation et Expérience</h3>
    <ul>
        <li>Formation supérieure en logistique, gestion administrative ou domaine connexe (Bac+2 minimum)</li>
        <li>Expérience minimale de 3 ans dans un poste similaire (idéalement dans le secteur associatif)</li>
        <li>Connaissance des enjeux logistiques propres aux organisations humanitaires</li>
        <li>Maîtrise des techniques de gestion des achats et approvisionnements</li>
    </ul>

    <h3>Compétences Techniques</h3>
    <ul>
        <li>Excellentes capacités en planification et organisation logistique</li>
        <li>Maîtrise des outils de gestion de stocks et d'inventaire</li>
        <li>Compétences en maintenance basique et supervision de prestataires techniques</li>
        <li>Aptitude à élaborer et suivre un budget</li>
        <li>Maîtrise des outils bureautiques et logiciels de gestion</li>
    </ul>

    <h3>Qualités Humaines et Relationnelles</h3>
    <ul>
        <li>Sens aigu de l'organisation et méthodologie</li>
        <li>Réactivité et capacité d'adaptation face aux imprévus</li>
        <li>Aptitude à travailler sous pression et à gérer les priorités</li>
        <li>Rigueur et souci du détail dans le suivi des opérations</li>
        <li>Bonnes capacités de négociation et de communication</li>
    </ul>

    <h3>Valeurs et Engagement</h3>
    <ul>
        <li>Adhésion aux valeurs chrétiennes de l'ONG</li>
        <li>Intégrité et transparence dans la gestion des ressources</li>
        <li>Esprit de service et orientation solutions</li>
        <li>Sens de la responsabilité et fiabilité</li>
        <li>Engagement pour l'optimisation et la bonne intendance des ressources</li>
    </ul>

    <h2>OBJECTIFS ET INDICATEURS DE PERFORMANCE</h2>

    <h3>Objectifs Quantitatifs</h3>
    <ul>
        <li>Mise en place d'un système d'inventaire complet d'ici septembre 2025</li>
        <li>Réduction des coûts opérationnels de 15% à qualité constante</li>
        <li>100% des événements et activités logistiquement soutenus sans incident majeur</li>
        <li>Établissement d'un réseau de 20 fournisseurs et prestataires de qualité</li>
        <li>Zéro rupture de stock critique pour les fournitures essentielles</li>
    </ul>

    <h3>Indicateurs Qualitatifs</h3>
    <ul>
        <li>Satisfaction des équipes quant aux conditions matérielles de travail</li>
        <li>Réactivité et efficacité dans la résolution des problèmes logistiques</li>
        <li>Qualité et fiabilité des équipements et services fournis</li>
        <li>Pertinence et efficience des procédures de gestion mises en place</li>
        <li>Contribution à la sécurité et au confort des équipes et bénéficiaires</li>
    </ul>

