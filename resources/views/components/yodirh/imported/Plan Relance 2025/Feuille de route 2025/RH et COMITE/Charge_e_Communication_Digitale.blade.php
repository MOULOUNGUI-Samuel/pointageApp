            </div>
            <p class="mb-4">Dans le cadre du Plan de Relance de l'ONG Néhémie International (juin-décembre 2025), le poste de <strong>Chargé(e) de la Communication Digitale</strong> occupe une place stratégique au sein de l'axe "Refonte de la communication et de la visibilité". Cette fonction est essentielle pour reconstruire l'image de l'ONG, amplifier son message et mobiliser efficacement les soutiens dans l'environnement numérique.</p>
            
            <div class="quote p-4 my-4 italic">
                <p>"Que votre lumière luise ainsi devant les hommes, afin qu'ils voient vos bonnes œuvres, et qu'ils glorifient votre Père qui est dans les cieux." - Matthieu 5:16</p>
            </div>
            
            <p>Le/la titulaire de ce poste a pour mission de concevoir, mettre en œuvre et évaluer la stratégie de communication digitale de l'ONG, afin de restaurer sa visibilité, d'engager ses audiences cibles et de soutenir la réalisation de sa mission sociale et spirituelle.</p>
        </section>

