                    <li><strong>Explorer de nouvelles plateformes ou formats</strong> adaptés à la mission de l'ONG</li>
                    <li><strong>Suivre les actions des organisations comparables</strong> pour identifier les bonnes pratiques</li>
                    <li><strong>Proposer des approches innovantes</strong> pour maximiser l'impact avec des ressources limitées</li>
                    <li><strong>Former les membres de l'équipe</strong> aux bonnes pratiques du digital</li>
                </ul>
            </div>
