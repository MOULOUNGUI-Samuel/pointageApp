            </div>

            <!-- Mission 1 -->
            <div class="mb-6">
                <div class="subsection-header p-2 mb-3">
                    <h4 class="font-semibold">1. ÉLABORATION ET MISE EN ŒUVRE DE LA STRATÉGIE DIGITALE</h4>
                </div>
                <ul class="list-disc pl-8 space-y-2">
                    <li><strong>Finaliser la stratégie de communication digitale</strong> en l'alignant sur les objectifs du plan de relance et les valeurs chrétiennes de l'ONG</li>
                    <li><strong>Créer et maintenir un écosystème digital cohérent</strong> (site web, réseaux sociaux, newsletter, etc.)</li>
                    <li><strong>Établir un calendrier éditorial</strong> structuré et adapté aux différentes plateformes</li>
                    <li><strong>Optimiser le référencement</strong> et la visibilité des contenus en ligne</li>
                    <li><strong>Analyser les performances</strong> des actions digitales et ajuster la stratégie en conséquence</li>
                </ul>
            </div>
            
            <!-- Mission 2 -->
            <div class="mb-6">
                <div class="subsection-header p-2 mb-3">
                    <h4 class="font-semibold">2. CRÉATION ET GESTION DE CONTENU</h4>
                </div>
                <ul class="list-disc pl-8 space-y-2">
                    <li><strong>Produire des contenus engageants et inspirants</strong> (textes, visuels, vidéos) respectant la charte graphique</li>
                    <li><strong>Gérer la mise en ligne</strong> et l'animation des plateformes sociales (Facebook, Instagram, YouTube, LinkedIn)</li>
                    <li><strong>Coordonner la série "La Voix du Bâtisseur"</strong> et autres formats récurrents sur les plateformes digitales</li>
                    <li><strong>Adapter les contenus aux différents formats</strong> et publics cibles</li>
                    <li><strong>Développer une bibliothèque de ressources digitales</strong> (photos, vidéos, témoignages, infographies)</li>
                    <li><strong>Intégrer les valeurs chrétiennes</strong> de façon accessible et pertinente dans les communications</li>
                </ul>
            </div>
            
            <!-- Mission 3 -->
            <div class="mb-6">
                <div class="subsection-header p-2 mb-3">
                    <h4 class="font-semibold">3. DÉVELOPPEMENT DE LA COMMUNAUTÉ EN LIGNE</h4>
                </div>
                <ul class="list-disc pl-8 space-y-2">
                    <li><strong>Animer la communauté digitale</strong> et favoriser les interactions avec les contenus</li>
                    <li><strong>Répondre aux messages et commentaires</strong> avec professionnalisme et bienveillance</li>
                    <li><strong>Identifier et mobiliser des ambassadeurs digitaux</strong> pour amplifier les messages</li>
                    <li><strong>Organiser des événements digitaux</strong> (webinaires, directs, campagnes participatives)</li>
                    <li><strong>Développer une base de données des contacts</strong> en ligne dans le respect des normes RGPD</li>
                </ul>
            </div>
            
            <!-- Mission 4 -->
            <div class="mb-6">
                <div class="subsection-header p-2 mb-3">
                    <h4 class="font-semibold">4. SOUTIEN AUX ACTIONS DE COLLECTE DE FONDS</h4>
                </div>
                <ul class="list-disc pl-8 space-y-2">
                    <li><strong>Concevoir et mettre en œuvre des campagnes digitales</strong> de collecte de fonds</li>
                    <li><strong>Optimiser l'expérience donateur en ligne</strong> (formulaires, pages de dons, remerciements)</li>
                    <li><strong>Développer des contenus spécifiques</strong> pour valoriser les projets auprès des donateurs potentiels</li>
                    <li><strong>Soutenir la promotion digitale</strong> du Dîner de Gala "GLOIRE" et autres événements de collecte</li>
                    <li><strong>Analyser l'efficacité des campagnes</strong> et proposer des améliorations continues</li>
                </ul>
            </div>
            
            <!-- Mission 5 -->
            <div class="mb-6">
                <div class="subsection-header p-2 mb-3">
                    <h4 class="font-semibold">5. VEILLE ET INNOVATION</h4>
                </div>
