            <h2 class="text-2xl font-bold">PARTIE 1 : CONCEPT DE LA CAMPAGNE</h2>

            <div class="strategy-section">
                <h3 class="text-xl font-semibold">1.1 Vision et fondement</h3>
                <p>La campagne "Donnez-leur vous-mêmes à manger" s'inspire directement de l'injonction du Christ aux disciples face à une foule affamée (Matthieu 14:16). Ce récit évangélique illustre parfaitement notre double mission :</p>
                <ul class="list-disc ml-6 mt-2">
                    <li>Répondre concrètement aux besoins matériels urgents (faim, précarité)</li>
                    <li>Encourager une transformation spirituelle profonde chez les donateurs</li>
                </ul>
                <p class="mt-2">Cette campagne n'est pas simplement une collecte de fonds, mais une invitation à vivre un acte de foi transformateur, où le sacrifice volontaire devient source de bénédiction pour le donateur lui-même.</p>
            </div>

            <div class="strategy-section">
                <h3 class="text-xl font-semibold">1.2 Message central</h3>
                <p>Le message pivot de la campagne s'articule autour d'une image puissante et accessible : <strong>"MOINS POUR MOI, LA VIE POUR EUX"</strong>.</p>
                
                <p class="mt-2">L'illustration centrale est celle d'un parent qui modifie l'allocation mensuelle de son enfant : non pas pour punir, mais pour enseigner le partage comme valeur fondamentale. L'enfant apprend ainsi qu'en acceptant de vivre avec un peu moins, il permet à d'autres de simplement vivre.</p>
                
                <p class="mt-2">Cette métaphore familiale permet de :</p>
                <ul class="list-disc ml-6 mt-2">
                    <li>Expliquer le principe du sacrifice significatif (au-delà du simple don du superflu)</li>
                    <li>Rendre concret le concept de transmission des valeurs chrétiennes</li>
                    <li>Créer une identification émotionnelle immédiate</li>
                </ul>
            </div>

            <div class="strategy-section">
                <h3 class="text-xl font-semibold">1.3 Objectifs de la campagne</h3>
                <p><strong>Objectif matériel</strong> : Collecter suffisamment de fonds en juin pour financer une distribution alimentaire substantielle en juillet 2025.</p>
                
                <p class="mt-2"><strong>Objectifs spirituels</strong> :</p>
                <ul class="list-disc ml-6 mt-2">
                    <li>Développer une culture du don sacrificiel plutôt que du don occasionnel</li>
                    <li>Sensibiliser les donateurs à la dimension spirituelle du partage</li>
                    <li>Ancrer l'action de NÉHÉMIE International dans une démarche d'évangélisation par les actes</li>
                </ul>
            </div>

            <div class="strategy-section">
                <h3 class="text-xl font-semibold">1.4 Éléments identitaires de la campagne</h3>
                <p><strong>Baseline</strong> : "Donnez-leur vous-mêmes à manger"</p>
                
                <p class="mt-2"><strong>Identité visuelle</strong> : Utilisation des couleurs officielles de NÉHÉMIE International avec une prédominance du bleu ciel (#5DAEDF) symbolisant l'espoir, associé au brun (#5D4037) évoquant la terre et le pain.</p>
                
                <p class="mt-2"><strong>Signature visuelle</strong> : Mains qui partagent du pain, symbolisant à la fois le don matériel et la communion spirituelle.</p>
                
                <p class="mt-2"><strong>Ton de communication</strong> : Inspirant sans être culpabilisant, ancré dans la foi sans prosélytisme, personnel et chaleureux.</p>
            </div>

            <div class="strategy-section">
                <h3 class="text-xl font-semibold">1.5 Promesse aux donateurs</h3>
                <p>Notre engagement envers les donateurs comporte trois dimensions :</p>
                <ul class="list-disc ml-6 mt-2">
                    <li><strong>Spirituelle</strong> : "Votre don est un acte de foi qui vous transforme autant qu'il transforme ceux qui reçoivent"</li>
                    <li><strong>Opérationnelle</strong> : "Chaque don effectué en juin sera utilisé pour une action concrète et visible en juillet"</li>
                    <li><strong>Relationnelle</strong> : "Vous ne donnez pas simplement à une cause, mais vous rejoignez une communauté engagée"</li>
                </ul>
            </div>

