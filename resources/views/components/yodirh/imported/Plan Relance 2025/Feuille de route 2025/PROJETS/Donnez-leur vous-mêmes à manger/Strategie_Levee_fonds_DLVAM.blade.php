            <div class="strategy-section">
                <h3 class="text-xl font-semibold">2.4 Tactiques de mobilisation spécifiques</h3>
                
                <h4>2.4.1 Stratégie grands donateurs</h4>
                <ul class="list-disc ml-6">
                    <li>Identification préalable de 10-15 donateurs à fort potentiel</li>
                    <li>Dossier de présentation personnalisé</li>
                    <li>Approche en trois temps : contact personnel, rencontre, suivi</li>
                    <li>Proposition d'engagement dans la durée plutôt que don ponctuel</li>
                </ul>
                
                <h4>2.4.2 Stratégie donateurs réguliers</h4>
                <ul class="list-disc ml-6">
                    <li>Concept des "Bâtisseurs mensuels" avec différents niveaux</li>
                    <li>Système progressif : d'abord un don ponctuel, puis proposition d'engagement régulier</li>
                    <li>Communication sur l'impact à long terme des dons réguliers</li>
                    <li>Mécanisme de parrainage par réseau de proximité</li>
                </ul>
                
                <h4>2.4.3 Mobilisation des églises partenaires</h4>
                <ul class="list-disc ml-6">
                    <li>Kit clé en main pour les pasteurs (annonces, prière, collecte)</li>
                    <li>Coordination des "dimanches de la générosité" (16 et 23 juin)</li>
                    <li>Supports pédagogiques pour école du dimanche sur le thème du partage</li>
                    <li>Témoignages dans les groupes de maison et cellules de prière</li>
                </ul>
                
                <h4>2.4.4 Facteurs d'engagement émotionnel</h4>
                <ul class="list-disc ml-6">
                    <li>Utilisation de l'histoire du parent qui apprend le partage à son enfant</li>
                    <li>Témoignages authentiques de bénéficiaires (passés ou potentiels)</li>
                    <li>Transparence sur l'utilisation prévue des fonds</li>
                    <li>Création d'un sentiment de communauté autour de la campagne</li>
                </ul>
            </div>

            <div class="strategy-section">
                <h3 class="text-xl font-semibold">2.5 Gestion des dons</h3>
                
                <h4>2.5.1 Circuits de collecte</h4>
                <ul class="list-disc ml-6">
                    <li><strong>Mobile money</strong> : Numéro dédié avec procédure simplifiée</li>
                    <li><strong>Transferts bancaires</strong> : Compte spécifique pour traçabilité</li>
                    <li><strong>Collectes physiques</strong> : Lors des réunions d'église (enveloppes dédiées)</li>
                    <li><strong>Dons en nature</strong> : Procédure de réception et valorisation</li>
                </ul>
                
                <h4>2.5.2 Traitement et suivi</h4>
                <ul class="list-disc ml-6">
                    <li>Enregistrement quotidien des dons dans l'outil centralisé</li>
                    <li>Émission systématique de reçus/remerciements dans les 24h</li>
                    <li>Catégorisation des donateurs pour suivi adapté</li>
                    <li>Tableau de bord quotidien partagé avec le comité de pilotage</li>
                </ul>
                
                <h4>2.5.3 Sécurisation des fonds</h4>
                <ul class="list-disc ml-6">
                    <li>Procédures de double validation pour les manipulations</li>
                    <li>Enregistrement comptable rigoureux et transparent</li>
                    <li>Sanctuarisation des fonds pour l'usage exclusif de la distribution</li>
                    <li>Reporting financier hebdomadaire au bureau exécutif</li>
                </ul>
            </div>

            <div class="strategy-section">
                <h3 class="text-xl font-semibold">2.6 Suivi, évaluation et adaptabilité</h3>
                
                <h4>2.6.1 Indicateurs de performance à suivre</h4>
                <ul class="list-disc ml-6">
                    <li>Nombre de donateurs uniques</li>
                    <li>Montant moyen par don</li>
                    <li>Taux de conversion des sollicitations</li>
                    <li>Engagement sur les contenus (partages, commentaires)</li>
