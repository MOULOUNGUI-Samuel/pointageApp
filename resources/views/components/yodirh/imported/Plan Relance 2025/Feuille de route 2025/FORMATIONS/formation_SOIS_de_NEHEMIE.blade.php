        <section id="presentation">
            <h3 class="section-title">Présentation du Programme SOIS</h3>
            <p>Bienvenue dans le programme <strong>SOIS</strong> de Néhémie International, un parcours de Formation, Accompagnement et Développement (FAD) dédié à la jeunesse. Guidé par le principe fondamental <strong>"Ton identité, ta mission."</strong>, ce programme est conçu pour aider chaque jeune à découvrir et à embrasser pleinement qui il est en Christ, afin de vivre la mission unique que Dieu lui a confiée.</p>
            <p>Enraciné dans les valeurs chrétiennes, notre objectif est de réduire le chômage, de promouvoir l'indépendance financière et l'entrepreneuriat, tout en transmettant des compétences pratiques adaptées au contexte local. Notre approche est holistique, intégrant enseignements bibliques et développement personnel pour une transformation durable, en accord avec notre mission de promouvoir l'Évangile de Jésus-Christ par des actions concrètes.</p>
        </section>

        <section id="concept">
            <h3 class="section-title">1. Concept Global de Formation</h3>
            <p>Le programme <strong>SOIS</strong> est structuré en <strong>modules hebdomadaires progressifs</strong>, appelés "Classes". Chaque classe s'étend sur une semaine (ou une période définie) et se concentre sur un thème inspiré par une figure biblique emblématique qui guide les apprentissages. L'objectif est de créer un parcours cohérent qui commence par la fondation de l'identité (le "SOIS qui tu es"), évolue vers l'acquisition de compétences pour accomplir ta mission, et culmine avec le leadership et l'impact communautaire.</p>
            <p>Chaque semaine combine :</p>
            <ul>
                <li><strong>Enseignements bibliques :</strong> Pour ancrer les valeurs, la vision et la compréhension de son identité et de sa mission en Christ.</li>
                <li><strong>Ateliers pratiques :</strong> Pour développer des compétences concrètes.</li>
                <li><strong>Temps de partage et de mentorat :</strong> Pour l'accompagnement personnalisé.</li>
                <li><strong>Mises en situation et projets :</strong> Pour appliquer les acquis.</li>
            </ul>
            <p>L'ensemble du programme est conçu pour être une expérience de transformation, encourageant les jeunes à "se lever et bâtir" leur avenir et celui de leur communauté sur la fondation solide de leur identité en Dieu et la clarté de leur mission.</p>
        </section>

        <section id="classes">
            <h3 class="section-title">2. Classes Hebdomadaires du Programme SOIS</h3>

            <div class="class-module">
                <h3>Classe 1 : ESTHER - "Découvrir son Identité Royale"</h3>
                <p class="signification"><strong>Signification spirituelle et symbolique :</strong> S'inspirant de la reine Esther, cette classe initiale du programme SOIS se concentre sur la découverte de son identité véritable en Dieu ("SOIS qui Il dit que tu es"). Elle met en lumière la valeur inestimable de chaque jeune, son but divin même face aux défis, et le courage issu de la foi pour répondre à l'appel de Dieu (Esther 4:14). L'objectif est de fonder sa mission sur une identité solide en Christ.</p>
                <h4>Thématiques de la semaine :</h4>
                <ul>
                    <li>Identité et estime de soi en Christ : "Je suis celui/celle que Dieu dit que je suis" (Genèse 1:27, Éphésiens 2:10, Psaume 139).</li>
                    <li>Découverte des dons et talents uniques : les capacités que Dieu m'a données pour ma mission (1 Corinthiens 12, Romains 12).</li>
                    <li>Discerner sa vocation et l'appel de Dieu pour sa vie (Jérémie 29:11, Proverbes 16:3).</li>
                    <li>Le courage de se lever : surmonter la peur et l'intimidation par la foi (Josué 1:9).</li>
                    <li>Établir une vision personnelle alignée sur son identité et les desseins de Dieu.</li>
                </ul>
            </div>

            <div class="class-module">
                <h3>Classe 2 : JOSEPH - "Marcher dans sa Mission avec Sagesse"</h3>
                <p class="signification"><strong>Signification spirituelle et symbolique :</strong> Basée sur la vie de Joseph, cette classe du programme SOIS met l'accent sur la fidélité dans ce que Dieu nous confie pour accomplir notre mission. Elle enseigne la sagesse dans la gestion des ressources, la persévérance face à l'adversité et la préparation divine pour de plus grandes responsabilités (Genèse 37-50), manifestant ainsi son identité par des actions concrètes.</p>
                <h4>Thématiques de la semaine :</h4>
                <ul>
                    <li>Communication efficace et relations interpersonnelles saines pour collaborer dans sa mission.</li>
                    <li>Gestion du temps, organisation et planification selon les priorités divines (Éphésiens 5:15-16, Proverbes 21:5).</li>
                    <li>L'intégrité et l'éthique chrétienne au travail : refléter Christ dans l'accomplissement de sa mission (Colossiens 3:23).</li>
                    <li>Développer la résilience et apprendre des épreuves avec le soutien de Dieu (Jacques 1:2-4, Romains 5:3-5).</li>
                    <li>Acquérir des compétences pratiques pour servir efficacement (selon le contexte local).</li>
                </ul>
            </div>

            <div class="class-module">
                <h3>Classe 3 : LYDIA - "Entreprendre sa Mission avec Foi"</h3>
                <p class="signification"><strong>Signification spirituelle et symbolique :</strong> Inspirée par Lydia (Actes 16), cette classe du programme SOIS est dédiée à l'entrepreneuriat vécu comme une expression de sa foi et une partie de sa mission. Elle montre comment Dieu peut ouvrir les cœurs et bénir les initiatives commerciales menées avec foi, générosité et hospitalité, transformant le travail en un ministère qui accomplit les desseins de Dieu.</p>
                <h4>Thématiques de la semaine :</h4>
                <ul>
                    <li>Principes bibliques de la gestion financière : être un bon gérant pour financer sa mission (Proverbes 3:9-10, Luc 16:10-11).</li>
                    <li>Identifier les opportunités d'affaires et développer une idée entrepreneuriale alignée avec sa mission.</li>
                    <li>Créer un plan d'affaires simple et pertinent.</li>
                    <li>Marketing, vente et service client avec intégrité et excellence, comme pour le Seigneur.</li>
                    <li>Le rôle de la prière et de la foi dans l'entrepreneuriat ; être une bénédiction pour les autres dans sa mission.</li>
                </ul>
            </div>

            <div class="class-module">
                <h3>Classe 4 : NÉHÉMIE - "Bâtir sa Mission pour la Communauté"</h3>
                <p class="signification"><strong>Signification spirituelle et symbolique :</strong> Nommée d'après Néhémie, cette classe du programme SOIS se concentre sur le leadership serviteur pour étendre sa mission à un impact communautaire. Elle encourage à incarner son identité de bâtisseur en Christ, en se levant pour servir, restaurer et transformer sa communauté, manifestant ainsi l'amour de Dieu (Livre de Néhémie).</p>
                <h4>Thématiques de la semaine :</h4>
                <ul>
                    <li>Le leadership selon le cœur de Dieu : service, vision, compassion dans sa mission (Marc 10:42-45, Néhémie 2:17-18).</li>
                    <li>Mobiliser les ressources et les personnes pour un projet commun, en s'appuyant sur Dieu.</li>
                    <li>Gestion de projet appliquée à des initiatives communautaires porteuses de sa mission.</li>
                    <li>Surmonter l'opposition et les défis avec foi et persévérance (Néhémie 4).</li>
                    <li>Mentorat et transmission : aider les autres à découvrir leur identité et leur mission.</li>
                </ul>
            </div>
