                éducation, santé).</li>
              <li>Amélioration des indicateurs de bien-être communautaire.</li>
              <li>Nombre de donateurs et bénévoles mobilisés pour les actions
                sociales.</li>
              <li>Taux de satisfaction des bénéficiaires.</li>
            </ul>
