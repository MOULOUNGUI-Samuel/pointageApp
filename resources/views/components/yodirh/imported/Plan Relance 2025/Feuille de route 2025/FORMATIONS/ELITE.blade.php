        <section id="presentation">
            <h3 class="section-title">Présentation du Programme ÉLITE</h3>
            <p>Bienvenue dans le programme <strong>ÉLITE</strong> de Néhémie International, un parcours de formation d'excellence destiné à équiper une nouvelle génération d'accompagnateurs chrétiens. Guidés par notre devise, <strong>"La mission de relever, la noblesse de servir"</strong>, ce programme est conçu pour former des experts capables d'intervenir avec sagesse et efficacité auprès des entrepreneurs en développement, en structuration ou traversant des difficultés.</p>
            <p>Enraciné dans les valeurs d'excellence, de compassion, de transformation et de solidarité de Néhémie International, "ÉLITE" vise à outiller des hommes et des femmes de foi pour qu'ils deviennent des catalyseurs de restauration et de croissance durable dans le tissu entrepreneurial, manifestant ainsi l'amour de Christ en action.</p>
        </section>

        <section id="concept">
            <h3 class="section-title">1. Concept Global de Formation</h3>
            <p>Le programme "ÉLITE" est la suite logique et le niveau supérieur du parcours "SOIS", s'adressant à ceux qui se sentent appelés à une mission d'accompagnement spécialisé. Il vise à transformer des individus passionnés en accompagnateurs chevronnés, capables d'apporter un soutien stratégique, technique et spirituel de haut niveau.</p>
            <h4>Philosophie et Approche :</h4>
            <ul>
                <li><strong>Vocation au Service :</strong> Sélection basée sur la maturité spirituelle, le cœur de serviteur et le désir d'impacter positivement la vie des entrepreneurs.</li>
                <li><strong>Apprentissage Immersif et Pratique :</strong> Études de cas réels, simulations d'accompagnement, cliniques d'entreprises supervisées, et masterclasses avec des professionnels aguerris.</li>
                <li><strong>Développement Holistique :</strong>
                    <ul>
                        <li><strong>Compétences Techniques Pointues :</strong> Diagnostic stratégique, analyse financière pour PME, techniques de redressement, développement organisationnel.</li>
                        <li><strong>Maîtrise des Outils d'Accompagnement :</strong> Coaching avancé, mentorat stratégique, facilitation de crise.</li>
                        <li><strong>Leadership Spirituel et Éthique :</strong> Application de la sagesse biblique aux défis entrepreneuriaux, posture d'intégrité et discernement.</li>
                    </ul>
                </li>
                <li><strong>Fraternité d'Intervention :</strong> Création d'une communauté d'accompagnateurs "ÉLITE" unis, partageant savoirs et soutien mutuel.</li>
                <li><strong>Engagement pour la Restauration Durable :</strong> Viser la santé à long terme des entreprises et la transformation des entrepreneurs, conformément aux principes du Royaume.</li>
            </ul>
        </section>

        <section id="classes">
            <h3 class="section-title">2. Classes Hebdomadaires du Programme ÉLITE</h3>

            <div class="class-module">
                <h3>Classe 1 : JETHRO - Le Conseil Éclairé et la Structure du Secours</h3>
                <p class="signification"><strong>Signification Symbolique :</strong> Jethro conseilla Moïse sur la structuration de son leadership face à une charge écrasante (Exode 18). Cette classe établit les fondations de l'accompagnateur "ÉLITE" : comment structurer son intervention pour un impact maximal et offrir un conseil sage et désintéressé.</p>
                <h4>Thématiques de la semaine :</h4>
                <ul>
                    <li>La posture noble de l'accompagnateur "ÉLITE" : serviteur-leader, humilité, intégrité, confidentialité.</li>
                    <li>Diagnostic 360° de l'entreprise en difficulté ou en stagnation : outils d'analyse financière, opérationnelle et humaine.</li>
                    <li>Techniques avancées d'écoute active empathique et de questionnement stratégique.</li>
                    <li>Établir un "contrat de secours" : objectifs, rôles, limites et attentes.</li>
                    <li>Principes bibliques du conseil désintéressé et de la sagesse divine (Proverbes, Jacques 1:5).</li>
                </ul>
            </div>

            <div class="class-module">
                <h3>Classe 2 : DANIEL - Discernement Profond et Stratégie en Temps de Crise</h3>
                <p class="signification"><strong>Signification Symbolique :</strong> Daniel possédait une sagesse et un discernement exceptionnels pour interpréter les situations complexes et conseiller les rois, même sous pression (Livre de Daniel). Cette classe forme à discerner les enjeux cachés et à élaborer des stratégies de survie et de rebond.</p>
                <h4>Thématiques de la semaine :</h4>
                <ul>
                    <li>Discerner les signaux faibles et les dynamiques spirituelles derrière les crises entrepreneuriales.</li>
                    <li>Analyse de viabilité et scénarios de crise : évaluer les options avec lucidité.</li>
                    <li>Élaboration de plans de contingence et de stratégies de gestion de crise.</li>
                    <li>Le rôle de la prière et du jeûne dans la recherche de la direction divine pour l'entreprise.</li>
                    <li>Aider l'entrepreneur à maintenir sa vision et son leadership en pleine tempête.</li>
                </ul>
            </div>

            <div class="class-module">
                <h3>Classe 3 : APOLLOS - Fortification et Accélération Post-Crise ou pour la Croissance</h3>
                <p class="signification"><strong>Signification Symbolique :</strong> Apollos, homme éloquent et versé dans les Écritures, "arrosait" et fortifiait ce que d'autres avaient planté, contribuant à la croissance (Actes 18, 1 Cor 3). Cette classe se concentre sur la consolidation après une crise ou sur l'optimisation pour une croissance saine.</p>
                <h4>Thématiques de la semaine :</h4>
                <ul>
                    <li>Stratégies de développement et de diversification adaptées aux PME.</li>
                    <li>Optimisation des processus internes et amélioration de la productivité.</li>
                    <li>Renforcement de la structure financière et recherche de financements adaptés.</li>
                    <li>Développement des équipes et culture d'entreprise axée sur la résilience et l'excellence.</li>
                    <li>Nourrir la foi et la vision de l'entrepreneur pour une croissance durable.</li>
                </ul>
            </div>

            <div class="class-module">
                <h3>Classe 4 : ESDRAS - Redressement Intégral et Reconstruction des Fondations</h3>
                <p class="signification"><strong>Signification Symbolique :</strong> Esdras, le scribe, a joué un rôle clé dans la restauration de la loi et la reconstruction morale et spirituelle du peuple après l'exil (Livre d'Esdras). Cette classe aborde le redressement en profondeur, souvent nécessaire après une crise majeure.</p>
                <h4>Thématiques de la semaine :</h4>
                <ul>
                    <li>Méthodologies de redressement d'entreprise : diagnostic financier poussé, restructuration.</li>
                    <li>Aspects juridiques et sociaux du redressement (sensibilisation).</li>
                    <li>Reconstruire la confiance : avec les employés, fournisseurs, clients, partenaires.</li>
                    <li>Mettre en place une gouvernance saine et des processus de contrôle interne robustes.</li>
                    <li>Principes bibliques de la repentance, du pardon et de la reconstruction sur des bases saines.</li>
                </ul>
            </div>

            <div class="class-module">
                <h3>Classe 5 : ÉLISÉE - Solutions Divines et Créativité en Milieu Aride</h3>
                <p class="signification"><strong>Signification Symbolique :</strong> Le prophète Élisée fut un canal pour des solutions divines et créatives dans des situations de manque ou de péril (2 Rois 2, 4). Cette classe vise à équiper l'accompagnateur pour aider à trouver des issues là où tout semble bloqué.</p>
                <h4>Thématiques de la semaine :</h4>
                <ul>
                    <li>Stimuler la créativité et l'innovation pour trouver des solutions "out-of-the-box".</li>
                    <li>Techniques de "problem solving" et de prise de décision sous incertitude.</li>
                    <li>Mobiliser les ressources cachées ou sous-utilisées de l'entreprise.</li>
                    <li>Accompagner l'entrepreneur dans la foi pour des percées et des "miracles" entrepreneuriaux.</li>
                    <li>Gestion du stress et de la pression (pour l'accompagnateur et l'accompagné).</li>
                </ul>
            </div>

            <div class="class-module">
                <h3>Classe 6 : BARNABÉ - L'Art de Relever par l'Encouragement et le Réseau Solidaire</h3>
                <p class="signification"><strong>Signification Symbolique :</strong> Barnabé, "fils de l'encouragement", était expert pour relever, connecter et bâtir des ponts (Actes 4, 9, 11). Cette classe finale consolide la posture de l'accompagnateur "ÉLITE" et l'importance du réseau.</p>
                <h4>Thématiques de la semaine :</h4>
                <ul>
                    <li>L'accompagnement par l'encouragement : maintenir la motivation et la persévérance.</li>
                    <li>Construire et animer un réseau d'entraide entre les entrepreneurs accompagnés.</li>
                    <li>Le mentorat de pairs : comment les entrepreneurs peuvent s'entraider.</li>
                    <li>Préparer la "sortie" de l'accompagnement : autonomisation de l'entrepreneur.</li>
                    <li>La mission continue : devenir un ambassadeur de Néhémie International.</li>
                </ul>
            </div>
        </section>

        <section id="public">
            <h3 class="section-title">3. Public Cible & Prérequis</h3>
            <div class="prerequisites">
                <h4>À qui s'adresse le programme ÉLITE ?</h4>
                <p>Ce programme est destiné aux personnes ayant :</p>
                <ul>
                    <li>Suivi avec succès le programme "SOIS" de Néhémie International ou un parcours jugé équivalent.</li>
                    <li>Une maturité spirituelle avérée et un engagement chrétien actif.</li>
                    <li>Un désir profond et une vocation claire pour accompagner et servir les entrepreneurs.</li>
                    <li>Une capacité d'écoute, d'analyse et de synthèse.</li>
                    <li>Une expérience entrepreneuriale (même modeste) ou une forte affinité avec le monde de l'entreprise.</li>
                </ul>
                <h4>Processus de sélection :</h4>
                <p>L'admission au programme "ÉLITE" se fait sur dossier et entretien, afin d'évaluer l'adéquation du candidat avec la mission et les exigences du programme.</p>
            </div>
