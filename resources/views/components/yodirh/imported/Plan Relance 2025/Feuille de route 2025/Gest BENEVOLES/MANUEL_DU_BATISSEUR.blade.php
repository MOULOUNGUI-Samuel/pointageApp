                <h2 class="text-3xl font-bold text-center">MODULE 1: LA COMPASSION</h2>
                <p class="text-center text-xl mt-2">Le cœur qui écoute et agit</p>
            </div>

            <div class="objective-box">
                <h3 class="text-xl font-bold mb-2">Objectif général:</h3>
                <p>Comprendre que la véritable compassion selon Dieu ne s'arrête pas au sentiment d'empathie mais se traduit nécessairement par une action concrète au service des plus vulnérables.</p>
                
                <h3 class="text-xl font-bold mt-4 mb-2">Objectifs spécifiques:</h3>
                <ol class="list-decimal pl-6">
                    <li>Définir la compassion biblique comme une émotion divine qui conduit à l'action transformatrice</li>
                    <li>Analyser comment la compassion de Néhémie pour son peuple l'a poussé à sacrifier son confort personnel pour entreprendre une œuvre de restauration</li>
                    <li>Identifier les domaines où nous sommes appelés à exercer cette compassion active dans notre contexte actuel</li>
                </ol>
            </div>

            <div class="mb-6">
                <h3 class="text-xl font-bold module-title">Problématique</h3>
                <p>Dans un monde où l'indifférence et l'individualisme prévalent souvent, où les souffrances d'autrui peuvent être facilement ignorées ou réduites à des considérations superficielles, ce module pose la question suivante: sommes-nous réellement touchés par les détresses qui nous entourent au point d'être mobilisés à l'action sacrificielle? La compassion authentique se mesure-t-elle aux émotions ressenties ou aux actions concrètes entreprises pour soulager la souffrance?</p>
            </div>

            <div class="mb-6">
                <h3 class="text-xl font-bold module-title">La compassion active dans la vie de Néhémie</h3>
                
                <p>La compassion véritable ne se limite jamais à une simple émotion; elle est cette profonde douleur empathique qui nous pousse irrésistiblement à l'action. Le terme hébreu pour "compassion" (rachamim) dérive de la même racine que le mot "utérus", évoquant ainsi une tendresse viscérale, un amour maternel inconditionnel qui ne peut rester inactif face à la souffrance.</p>
                
                <div class="scripture">
                    <p>"Lorsque j'appris ces choses, je m'assis, je pleurai, et je fus plusieurs jours dans la désolation. Je jeûnai et je priai devant le Dieu des cieux." (Néhémie 1:4)</p>
                </div>
                
                <p>L'histoire de Néhémie commence par ce moment crucial où il reçoit des nouvelles de Jérusalem. Sa réaction immédiate révèle l'authenticité de sa compassion: il ne se contente pas de ressentir une tristesse passagère, mais il entre dans une désolation profonde qui le conduit au jeûne et à la prière. Ce n'est pas une émotion superficielle, mais une douleur qui l'habite et le transforme.</p>
                
                <p class="mt-4">La compassion de Néhémie est d'autant plus remarquable qu'il jouissait d'une position privilégiée à la cour du roi. Il aurait pu se contenter d'exprimer une sympathie distante pour ses compatriotes lointains. Mais sa compassion authentique ne pouvait s'accommoder du confort personnel face à la détresse collective.</p>
                
                <p class="mt-4">Cette compassion se manifeste en trois dimensions essentielles:</p>
                
                <ul class="list-disc pl-6 mt-2 space-y-2">
                    <li><span class="font-semibold">Une identification sincère</span> - Néhémie ne parle pas de "leurs" problèmes mais de "nos" murailles détruites, "notre" opprobre. Il s'identifie pleinement à la souffrance de son peuple.</li>
                    <li><span class="font-semibold">Une intercession fervente</span> - Sa compassion le conduit d'abord à plaider auprès de Dieu, reconnaissant que la restauration véritable commence par une œuvre divine.</li>
                    <li><span class="font-semibold">Un engagement coûteux</span> - Au chapitre 2, sa compassion franchit une étape décisive lorsqu'il demande au roi la permission de quitter son poste prestigieux pour aller reconstruire Jérusalem, risquant ainsi sa position et sa sécurité.</li>
                </ul>
                
                <div class="scripture mt-6">
                    <p>"Le roi me dit: Que demandes-tu? Je priai le Dieu des cieux, et je répondis au roi: Si le roi le trouve bon, et si ton serviteur lui est agréable, envoie-moi en Judée, vers la ville des sépulcres de mes pères, pour que je la rebâtisse." (Néhémie 2:4-5)</p>
                </div>
                
                <p class="mt-4">La compassion de Néhémie ne s'est pas arrêtée à cette décision initiale. Tout au long de son œuvre de reconstruction, il démontre une préoccupation constante pour les plus vulnérables. Au chapitre 5, confronté à l'exploitation des pauvres par les riches, il intervient avec fermeté:</p>
                
                <div class="scripture">
                    <p>"J'ai été très irrité quand j'ai entendu leurs plaintes et ces paroles-là... J'ai convoqué contre eux une grande assemblée... et je leur dis: Nous avons racheté nos frères les Juifs qui étaient vendus aux nations... et vous vendriez vous-mêmes vos frères, et c'est à nous qu'ils seraient vendus!" (Néhémie 5:6-8)</p>
                </div>
                
                <p class="mt-4">Cette intervention montre que la compassion véritable s'accompagne d'un sens aigu de la justice sociale. Néhémie ne se contente pas de rebâtir des murs; il œuvre à reconstruire une communauté fondée sur la dignité et la solidarité.</p>
            </div>

            <div class="application-box">
                <h3 class="text-xl font-bold mb-4">Application pour aujourd'hui</h3>
                
                <p>La compassion selon le modèle de Néhémie nous appelle à une transformation profonde de notre regard et de nos actes:</p>
                
                <h4 class="font-bold mt-4 mb-2">1. Développer une sensibilité authentique</h4>
                <p>Cultivons un cœur attentif aux souffrances qui nous entourent, refusant l'indifférence ou l'endurcissement. Demandons à Dieu de nous donner "des yeux qui voient et des oreilles qui entendent" les détresses de notre temps, qu'elles soient proches ou lointaines.</p>
                
                <h4 class="font-bold mt-4 mb-2">2. Passer du sentiment à l'action</h4>
                <p>Une compassion qui n'agit pas n'est pas la compassion du Christ. Identifiez un besoin concret dans votre entourage ou au sein de notre ONG NÉHÉMIE International et engagez-vous dans une action spécifique pour y répondre, même si cela implique un sacrifice personnel.</p>
                
                <h4 class="font-bold mt-4 mb-2">3. Pratiquer une compassion holistique</h4>
                <p>À l'image de Néhémie qui ne s'est pas contenté de reconstruire des murs mais a également restauré la justice sociale, notre compassion doit viser une transformation intégrale: adresser à la fois les besoins matériels, émotionnels et spirituels des personnes.</p>
                
                <h4 class="font-bold mt-4 mb-2">4. Ancrer notre compassion dans la prière</h4>
                <p>La véritable compassion prend sa source dans la communion avec le cœur de Dieu. Avant d'entreprendre toute action, suivons l'exemple de Néhémie en présentant les besoins à Dieu et en cherchant Sa direction.</p>
                
                <div class="mt-6 italic">
                    <p>"Soyez bons les uns envers les autres, compatissants, vous pardonnant réciproquement, comme Dieu vous a pardonné en Christ." (Éphésiens 4:32)</p>
                </div>
            </div>
