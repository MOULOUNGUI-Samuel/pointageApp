                <h3 class="text-xl font-bold mt-4 mb-2">Objectifs spécifiques:</h3>
                <ol class="list-decimal pl-6">
                    <li>Définir la diligence biblique comme une vertu alliant zèle, discipline, sagesse pratique et persévérance</li>
                    <li>Analyser comment la diligence de Néhémie s'est manifestée dans son leadership et sa gestion du projet de reconstruction</li>
                    <li>Identifier les obstacles à la diligence et développer des stratégies pour cultiver cette qualité dans notre service</li>
                </ol>
