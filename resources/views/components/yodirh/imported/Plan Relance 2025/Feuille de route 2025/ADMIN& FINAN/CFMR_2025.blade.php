        <h2 class="section-title">ÉTAT DES LIEUX ET OBJECTIFS</h2>
        
        <div class="card">
            <h3 class="text-lg font-bold">Situation Financière Actuelle (Juin 2025)</h3>
            <ul class="list-disc pl-6 mt-2">
                <li>Trésorerie proche de zéro</li>
                <li>Absence de sources de financement régulières</li>
                <li>Dépendance excessive à des dons ponctuels</li>
                <li>Projets phares (dont "Donnez-leur vous-même à manger") en suspens faute de ressources</li>
            </ul>
            
            <h3 class="text-lg font-bold mt-6">Contexte Local Spécifique</h3>
            <ul class="list-disc pl-6 mt-2">
                <li>Églises gabonaises majoritairement focalisées sur leur propre développement infrastructurel</li>
                <li>Culture locale du don moins structurée vers le soutien d'ONG confessionnelles</li>
                <li>Secteur privé gabonais aux ressources limitées par le contexte économique</li>
                <li>Nécessité d'approches innovantes et moins dépendantes des structures ecclésiales locales</li>
            </ul>
            
            <h3 class="text-lg font-bold mt-6">Objectifs Financiers du Plan de Relance</h3>
            <div class="objective">1. Mobiliser <strong>27 000 000 FCFA</strong> d'ici décembre 2025 pour relancer les activités essentielles</div>
            <div class="objective">2. Établir au moins <strong>trois sources de financement stables et diversifiées</strong></div>
            <div class="objective">3. Constituer une <strong>réserve opérationnelle</strong> couvrant 3 mois de fonctionnement</div>
            <div class="objective">4. Mettre en place une <strong>structure financière transparente et durable</strong></div>
        </div>

