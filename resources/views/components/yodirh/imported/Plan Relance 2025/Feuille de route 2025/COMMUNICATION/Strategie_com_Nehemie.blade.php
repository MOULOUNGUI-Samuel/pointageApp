        <section id="introduction">
            <h2>INTRODUCTION</h2>
            <p>L'ONG Néhémie International, organisation chrétienne gabonaise, agit pour répondre aux besoins spirituels et sociaux, former pour l'emploi et créer une communauté de « Bâtisseurs ». Face aux crises actuelles, ce plan de communication digitale vise à faire entendre la voix de l'espérance et de l'impact fondé sur la foi, en renforçant la visibilité de l'ONG, mobilisant les ressources et amplifiant son action, exclusivement via les canaux numériques.</p>
        </section>

        <section id="objectifs">
            <h2>I. OBJECTIFS DE COMMUNICATION DIGITALE</h2>
            <ol>
                <li><strong>Positionner Néhémie International</strong> comme une ONG chrétienne crédible, engagée et inspirée sur l'espace digital gabonais et au-delà.</li>
                <li><strong>Promouvoir activement ses actions</strong> sociales (FAD, "Donnez-leur vous-même à manger"), éducatives (Formations Néhémie, Club d'Anglais) et spirituelles (Prière du Bâtisseur, La Voix du Bâtisseur) via des contenus digitaux engageants.</li>
                <li><strong>Valoriser ses formats médiatiques digitaux</strong> (émission en ligne, podcasts, webinaires) et ses événements phares (en assurant une forte présence et couverture digitale).</li>
                <li><strong>Fédérer et développer une communauté digitale active</strong> de sympathisants, donateurs, bénévoles, bénéficiaires et partenaires.</li>
                <li><strong>Augmenter les soutiens financiers et les ressources</strong> (bénévolat, dons en nature) via des campagnes de levée de fonds et d'appels à l'action digitaux.</li>
            </ol>
        </section>

        <section id="axes-communication">
            <h2>II. AXES DE COMMUNICATION DIGITALE</h2>
            <div class="axe">
                <h3>A. AXE INSTITUTIONNEL : "Fondations Solides, Vision Claire"</h3>
                <p><strong>Objectif :</strong> Affirmer la crédibilité, la transparence et l'impact global de Néhémie International. Mettre en lumière la bonne gouvernance et la vision stratégique (Axe 2 de la stratégie NI).</p>
                <p><strong>Contenus types :</strong> Présentation de la mission/vision, rapports d'impact digitalisés (infographies, courtes vidéos), "portraits de bâtisseurs" (équipe, CA), actualités de l'ONG, informations sur la gouvernance, communiqués.</p>
            </div>
            <div class="axe">
                <h3>B. AXE ÉVANGÉLISTE : "Foi Active, Vies Transformées"</h3>
                <p><strong>Objectif :</strong> Proclamer l'Évangile de manière créative et accessible. Mettre en lumière l'impact de la foi dans la résolution des défis contemporains et la transformation individuelle. Nourrir la croissance spirituelle de la communauté (Axe 5 de la stratégie NI).</p>
                <p><strong>Contenus types :</strong> Émission "La Voix du Bâtisseur", "La Prière du Bâtisseur" en direct et résumés, versets/pensées inspirantes, témoignages de foi et de transformation, enseignements bibliques courts (vidéos, podcasts, articles de blog), Q&R spirituelles en ligne.</p>
            </div>
            <div class="axe">
                <h3>C. AXE SOCIAL : "Solidarité en Action, Avenirs Bâtis"</h3>
                <p><strong>Objectif :</strong> Mettre en avant les actions concrètes d'entraide, de formation et d'autonomisation. Mobiliser la communauté pour soutenir ces initiatives et valoriser les compétences acquises par les bénéficiaires (Axes 1, 3, 4 de la stratégie NI).</p>
                <p><strong>Contenus types :</strong> Reportages digitaux sur les projets (FAD, "Oser Bâtir", "Donnez-leur vous-même à manger"), témoignages de bénéficiaires (avant/après avec respect et consentement), mini-tutoriels issus des formations, appels aux dons/bénévolat ciblés pour les projets sociaux, promotion des ateliers de compétences et du Club d'Anglais.</p>
            </div>
