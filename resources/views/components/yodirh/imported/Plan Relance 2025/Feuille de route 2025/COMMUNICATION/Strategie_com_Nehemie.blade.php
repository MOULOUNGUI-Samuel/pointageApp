        <section id="actions-phares">
            <h2>VI. ACTIONS & DISPOSITIFS PHARES (DIGITAL FIRST)</h2>
            <h4>A. Émission "La Voix du Bâtisseur" (En ligne)</h4>
            <ul>
                <li><strong>Objectif :</strong> Illustrer l'implication de Dieu dans les défis contemporains.</li>
                <li><strong>Format :</strong> Enseignement biblique, témoignages sociaux, interviews.</li>
                <li><strong>Diffusion :</strong> Facebook Live, YouTube Premiere, IGTV (extraits), Podcast (audio).</li>
                <li><strong>Première émission :</strong> Lancée le 15 Mai 2025 (selon Comketing – la stratégie digitale de juin amplifiera sa promotion). Thème : "Quand la foi répond à la pauvreté".</li>
                <li><strong>Fréquence :</strong> 2 fois par mois.</li>
            </ul>

            <h4>B. "La Prière du Bâtisseur" (Mensuelle en ligne)</h4>
            <ul>
                <li><strong>Objectif :</strong> Rassembler pour la prière, intercéder pour l'ONG et la nation.</li>
                <li><strong>Prochaine date :</strong> Samedi 1er Juin 2025 (puis chaque 1er samedi du mois).</li>
                <li><strong>Contenu :</strong> Thème spirituel, Live Facebook/YouTube, résumé en image & versets, appel à l'intercession communautaire.</li>
            </ul>

            <h4>C. Formations en Outils de Productivité & FAD (Promotion et Contenus Digitaux)</h4>
            <ul>
                <li><strong>Objectif :</strong> Valoriser les formations pour l'employabilité et l'autonomie.</li>
                <li><strong>Public :</strong> Jeunes chercheurs d'emploi, nouveaux diplômés.</li>
                <li><strong>Promotion Digitale :</strong> Affiches digitales mensuelles, témoignages "avant/après" (vidéo/texte), mini-tutoriels vidéo (extraits des formations), webinaires d'introduction.</li>
                <li><strong>Fréquence :</strong> 2 sessions par mois (selon NI), promotion continue.</li>
            </ul>

            <h4>D. Dîner de Gala "GLOIRE" (Promotion, Couverture et Levée de Fonds Digitale)</h4>
            <ul>
                <li><strong>Date Prévue :</strong> Samedi 29 Novembre 2025.</li>
                <li><strong>Objectif :</strong> Célébrer, lever des fonds pour 2026.</li>
                <li><strong>Communication Digitale Spécifique :</strong>
                    <ul>
                        <li>Campagne visuelle en ligne (comptes à rebours, visuels des prix).</li>
                        <li>Teaser vidéo.</li>
                        <li>Page d'événement sur le site web avec billetterie/promesses de dons en ligne.</li>
                        <li>Couverture live (stories, extraits Facebook/YouTube si possible).</li>
                        <li>Aftermovie digital.</li>
                        <li>Campagne d'appel aux dons en ligne avant, pendant, et après l'événement.</li>
                    </ul>
                </li>
            </ul>
